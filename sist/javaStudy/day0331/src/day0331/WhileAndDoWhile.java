package day0331;

public class WhileAndDoWhile {
	public static void main(String[] args) {
		int i = 10;
		while (i<=5) {
			System.out.println("hello");
			i++;
		}
		
		System.out.println("--------------------------------------");
		int j =10;
		do {
			System.out.println("hello");
			j++;
		} while (j<=5);
	}
}
